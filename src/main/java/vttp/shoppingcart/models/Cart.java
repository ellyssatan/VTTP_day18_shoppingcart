package vttp.shoppingcart.models;

public class Cart {
    private String user;
    private String cartItems;

    
    public String getUser() {
        return user;
    }
    public void setUser(String user) {
        this.user = user;
    }
    public String getCartItems() {
        return cartItems;
    }
    public void setCartItems(String cartItems) {
        this.cartItems = cartItems;
    }

}
